// signed distance to a polygon set (even-odd holes), positive INSIDE
function sdf2d(si, x, y) {
  const polys = SECTIONS[si][1], metas = META[si];
  let d = Infinity, inside = false;
  for (let pi = 0; pi < polys.length; pi++) {
    const p = polys[pi], m = metas[pi];
    const bdx = x < m[0] ? m[0] - x : x > m[2] ? x - m[2] : 0;
    const bdy = y < m[1] ? m[1] - y : y > m[3] ? y - m[3] : 0;
    const lb = bdx * bdx + bdy * bdy;
    const n = p.length / 2;
    if (lb > d) {
      if (y >= m[1] && y <= m[3] && x <= m[2]) {
        for (let i = 0, j = n - 1; i < n; j = i++) {
          const yi = p[i * 2 + 1], yj = p[j * 2 + 1];
          if ((yi > y) !== (yj > y)) {
            const xi = p[i * 2], xj = p[j * 2];
            if (x < ((xj - xi) * (y - yi)) / (yj - yi) + xi) inside = !inside;
          }
        }
      }
      continue;
    }
    for (let i = 0, j = n - 1; i < n; j = i++) {
      const xi = p[i * 2], yi = p[i * 2 + 1], xj = p[j * 2], yj = p[j * 2 + 1];
      const dx = xj - xi, dy = yj - yi;
      const t = Math.max(0, Math.min(1, ((x - xi) * dx + (y - yi) * dy) / (dx * dx + dy * dy || 1e-12)));
      const px = xi + t * dx - x, py = yi + t * dy - y;
      const dd = px * px + py * py;
      if (dd < d) d = dd;
      if ((yi > y) !== (yj > y) && x < ((xj - xi) * (y - yi)) / (yj - yi) + xi) inside = !inside;
    }
  }
  return (inside ? 1 : -1) * Math.sqrt(d);
}

const CACHE = new Map();
function sdfAt(si, x, y) {
  let m = CACHE.get(si);
  if (!m) { m = new Map(); CACHE.set(si, m); }
  const key = Math.round(x * 1024) * 262144 + Math.round(y * 1024);
  let v = m.get(key);
  if (v === undefined) { v = sdf2d(si, x, y); m.set(key, v); }
  return v;
}

const ZLIST = SECTIONS.map((s) => s[0]);
function sdf(p) {
  const z = p[2];
  const capD = Math.min(z - Z0, Z1 - z); // flat end caps
  let lo = 0, hi = ZLIST.length - 1;
  if (z <= ZLIST[0]) return Math.min(sdfAt(0, p[0], p[1]), capD);
  if (z >= ZLIST[hi]) return Math.min(sdfAt(hi, p[0], p[1]), capD);
  while (hi - lo > 1) { const mid = (lo + hi) >> 1; if (ZLIST[mid] <= z) lo = mid; else hi = mid; }
  const t = (z - ZLIST[lo]) / (ZLIST[hi] - ZLIST[lo]);
  const a = sdfAt(lo, p[0], p[1]);
  const b = sdfAt(hi, p[0], p[1]);
  return Math.min((1 - t) * a + t * b, capD);
}

